add_action( 'save_post', __NAMESPACE__ . '\save_post_pdf', 10, 1 );

/**
 * Save post id in option to generate PDFs.
 *
 * @param int $post_id Post ID.
 */
function save_post_pdf( $post_id ) 
{
    if ( get_post_type( $post_id ) !== 'post' ) {
        return;
    }
    
    $option_key   = 'generate_post_pdf';
    $existing_ids = get_option( $option_key, '' );
    
    $ids = $existing_ids ? explode( ',', $existing_ids ) : [];
    
    if ( ! in_array( $post_id, $ids ) ) {
        $ids[] = $post_id;
        update_option( $option_key, implode( ',', $ids ) );
    }
}

/**
 * Generate PDFs for post. Generate Full PDF and Free Sample PDF. Update created PDF flag.
 *
 * @param int $post_id Post ID.
 * @return bool
 */
function set_pdfs_to_post( $post_id ) {

    $post = get_post( $post_id );

	// Only set for post_type = post!
	if ( 'post' !== $post->post_type ) {
		return;
	}
		
    // Variables comunes
    $prefixName           = 'pdf-';
    $prefixFreeSampleName = 'pdf-free-';
    $filePath             = get_path_pdf( $post_id );
    $fileName             = get_filename_pdf( $post_id, $prefixName );
    $fileFreeSampleName   = get_filename_pdf( $post_id, $prefixFreeSampleName );
    $file_original        = $filePath . $fileName;
    $file_free            = $filePath . $fileFreeSampleName;
    
    // get the HTML content of the post for the full PDF.
    $html = get_html_content( $post );
    
    if ( save_full_pdf( $filePath, $fileName, $html, $post_id ) ) {
        update_field( 'post_full_pdf', str_replace( wp_upload_dir()['basedir'], wp_upload_dir()['baseurl'], $file_original ), $post_id );
        update_field( 'post_create_pdf', 1, $post_id );
    }

    // Crear FREE SAMPLE PDF a partir de las páginas del Full PDF
    if ( save_free_pdf( $file_original, $file_free ) ) {
        update_field( 'post_free_pdf', str_replace( wp_upload_dir()['basedir'], wp_upload_dir()['baseurl'], $file_free ), $post_id );
    }
    return true;
}

/**
 * Get the path to save the PDFs.
 *
 * @param int $post_id Post ID.
 * @return string
 */
function get_path_pdf ( $post_id ) {
	$filepath = wp_upload_dir()['basedir'] . '/' . get_name_dir_pdf_files() . '/' . substr($post_id, -1, 1) . substr($post_id, 0, 1) . '/';
	return $filepath;
}

/**
 * Get the filename for the PDFs.
 *
 * @param int $post_id Post ID.
 * @param string $prefixName Prefix for the filename.
 * @return string
 */
function get_filename_pdf ( $post_id, $prefixName ) {
	$filename = $prefixName . $post_id . '-' . substr($post_id, -1, 1) . substr($post_id, 0, 1) . '.pdf'; 
	return $filename;
}

/** 
 * Get the name of the directory to save the PDF files.
 * 
 * @return string
 */
function get_name_dir_pdf_files () {
    $name = 'pdfs-downloads';
    if ( function_exists( 'get_field' ) ) {
        $name = get_field( 'pdf_path_files', 'option' );
    }
    return $name;
}

function save_full_pdf ($path, $file, $contentHtml, $post_id) {

	$pathFiles = get_stylesheet_directory();
	$pathUploads = wp_upload_dir()['basedir'];

	$options = new Options(); 
	$options->set( 'isPhpEnabled', 'true' );
	$options->set( 'isHtml5ParserEnabled', 'true' ); 
	$options->set( 'isRemoteEnabled', 'true' ); 
	// $options->set('isFontSubsettingEnabled', false);

	$options->set( 'chroot', $pathFiles . '/fonts' );
	$options->set( 'fontDir',  $pathFiles . '/fonts' );
	$options->set( 'defaultFont', 'bitter' );

	$options->set( 'tempDir',  $pathUploads . '/' . get_name_dir_pdf_files() . '/tmp' );
	$options->set( 'fontCache',  $pathUploads . '/' . get_name_dir_pdf_files() . '/tmp' );

	// TODO: Comentado para optimizar. Posiblemente se pueda eliminar.
	// ini_set( "display_errors", true );
	// ini_set( "error_log", "phperr.log" );
	// ini_set( "log_errors", true );
	// error_reporting( E_ALL );

	// TODO: Comentado para optimizar. Posiblemente se pueda eliminar.
	// $_dompdf_show_warnings = true; // record warnings generated by Dompdf
	// $_dompdf_debug = true; // output frame details for every frame in the document
	// $_DOMPDF_DEBUG_TYPES = [
	// 	'page-break' => false // record information about page break determination
	// ];

	// TODO: Comentado para optimizar. Posiblemente se pueda eliminar.
	// $options->set( 'logOutputFile', $pathUploads . '/' . get_name_dir_pdf_files() . '/tmp/log.html' );
	// $options->set( 'debugPng', false );
	// $options->set( 'debugKeepTemp', false );
	// $options->set( 'debugCss', false );
