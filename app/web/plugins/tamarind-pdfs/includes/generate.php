	// $options->set( 'debugLayoutBlocks', false );
	// $options->set( 'debugLayoutInline', false );
	// $options->set( 'debugLayoutPaddingBox', false );
	$options->set( 'renderer', 'CPDF' );

	$dompdf = new Dompdf( $options );
	$dompdf->loadHtml( $contentHtml, 'UTF-8' );
	$dompdf->setPaper( 'A4', 'portrait' );
	$dompdf->setHttpContext([
		'ssl' => [
			'verify_peer' => FALSE,
			'verify_peer_name' => FALSE,
			'allow_self_signed'=> TRUE
		]
	]);
	$dompdf->render();

	$canvas = $dompdf->getCanvas();
	$canvas->page_script( function ($pageNumber, $pageCount, $canvas, $fontMetrics ) use ( $post_id, $pathFiles ) {
		
		$text = "Page $pageNumber / $pageCount";
		$font = $fontMetrics->getFont( 'Bitter', "normal" ); 
		$pageWidth = $canvas->get_width();
		$pageHeight = $canvas->get_height();
		$size = 10;
		$width = $fontMetrics->getTextWidth( $text, $font, $size );

		switch ( $pageNumber ) {
			case 1:
				// $canvas->text($pageWidth - $width - 20, $pageHeight - 20, 'PORTADA!', $font, $size);
				break;
			case $pageCount:
                if ( function_exists( 'get_field' ) ) {
                    $imageURL = get_field( 'pdf_header_logo', 'options' );
					$imageURL = get_path_url_image( $imageURL );
                } 
                if ( ! $imageURL ) {
                    $imageURL = $pathFiles . '/images/pdf_logo.jpg';
                }
				$imgWidth = 25; 
				$imgHeight = 25;
				$canvas->image( $imageURL, 10, $pageHeight - $imgHeight - 10, $imgWidth, $imgHeight, $resolution = "normal" );

				$textFooter = '';
                if ( function_exists( 'get_field' ) ) {
                    $textFooter = get_field( 'footer_text_left_logo', 'options' );
                }
				$sizeFooter = 10;
				$font = $fontMetrics->getFont( 'Bitter', "normal" );
				$heightFooter = $fontMetrics->getFontHeight( $font, $sizeFooter );
                if ( $textFooter ) {
                    // $widthFooter = $fontMetrics->getTextWidth( $textFooter, $font, $sizeFooter );
    				$canvas->text( $imgWidth + 12, $pageHeight - 12 - ($imgHeight/2) - ($heightFooter/2), $textFooter, $font, $sizeFooter);
                }
				$canvas->text( $pageWidth - $width - 20, $pageHeight - 12 - ($imgHeight/2) - ($heightFooter/2), $text, $font, $size );
				break;
			default:
                if ( function_exists( 'get_field' ) ) {
                    $imageURL = get_field( 'pdf_header_image', 'options' );
					$imageURL = get_path_url_image( $imageURL );
                } 
                if ( ! $imageURL ) {
                    $imageURL = $pathFiles . '/images/pdf_header.jpg';
                }
				$imgWidth = 1034; 
				$imgHeight = 50;
				$canvas->image( $imageURL, 0, 0, $imgWidth, $imgHeight, $resolution = "normal" );

				$post = get_post( $post_id );
				$titlePost = $post->post_title;

				$sizeHeader = 9;
				$font = $fontMetrics->getFont( 'Bitter', "400" ); 
				$widthHeader = $fontMetrics->getTextWidth( $titlePost, $font, $sizeHeader );
				$heightFooter = $fontMetrics->getFontHeight( $font, $sizeHeader );
				$canvas->text( ($pageWidth/2) - ($widthHeader/2), 8, $titlePost, $font, $sizeHeader, $color = array(255,255,255) );

                if ( function_exists( 'get_field' ) ) {
                    $imageURL = get_field( 'pdf_header_logo', 'options' );
					$imageURL = get_path_url_image( $imageURL );
                } 
                if ( ! $imageURL ) {
                    $imageURL = $pathFiles . '/images/pdf_logo.jpg';
                }
				$imgWidth = 25; 
				$imgHeight = 25;
				$canvas->image( $imageURL, 10, $pageHeight - $imgHeight - 10, $imgWidth, $imgHeight, $resolution = "normal" );

				$textFooter = '';
                if ( function_exists( 'get_field' ) ) {
                    $textFooter = get_field( 'footer_text_left_logo', 'options' );
                }
				$sizeFooter = 10;
				$font = $fontMetrics->getFont( 'Bitter', "normal" ); 
				$heightFooter = $fontMetrics->getFontHeight( $font, $sizeFooter );
                if ( $textFooter ) {
                    // $widthFooter = $fontMetrics->getTextWidth( $textFooter, $font, $sizeFooter );
    				$canvas->text( $imgWidth + 12, $pageHeight - 12 - ($imgHeight/2) - ($heightFooter/2), $textFooter, $font, $sizeFooter);
                }
				$canvas->text( $pageWidth - $width - 20, $pageHeight - 12 - ($imgHeight/2) - ($heightFooter/2), $text, $font, $size );
				break;
		}
	});
	$output = $dompdf->output();

	if (!is_dir($path)) {
		// dir doesn't exist, make it
		mkdir($path, 0775, true);
	}
	$r = file_put_contents($path . $file, $output);
	// exit;

	if ($r) {
		return true;
	} 
	return false;
}

/**
 * Generate the HTML content for the Free PDF from the original PDF.
 *
 * @param string $file_original Path to the original PDF.
 * @param string $file_free Path to the free PDF.
 * @return bool|string
 */
function save_free_pdf ( $file_original, $file_free ) {
    // initiate FPDI
	$pdf = new Fpdi();
	$pageCount = $pdf->setSourceFile( $file_original );
	// agregar las páginas deseadas
	$pagesToAdd = [1, 2, 4, $pageCount]; 

	// array_unique para eliminar duplicados
	// array_filter para eliminar los valores que sean mayores que el número de páginas del PDF
	$pagesToAdd = array_filter(array_unique( $pagesToAdd ), fn($i) => ( $i <= $pageCount ) );
	foreach( $pagesToAdd as $page ) {
		$pdf->AddPage();
		$tplIdx = $pdf->importPage( $page );
		$pdf->useTemplate( $tplIdx );
	}
	$pdf->Output( $file_free, 'F' );
	return true;
}
